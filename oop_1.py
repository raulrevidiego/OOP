class telefono:
  def __init__(self, marca, color, modelo, memoria):
    self.marca = marca
    self.color = color
    self.modelo = modelo
    self.memoria = memoria
  def llamar(self):
    print(f"Llamando desde tu {self.marca} {self.modelo}")
  def colgar(self):
    print(f"Colgando desde tu {self.marca} {self.modelo}")
  def foto(self):
    print(f"Tomando foto con tus {self.modelo}")

